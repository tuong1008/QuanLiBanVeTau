    private boolean xoaVeTrongDB(int maVe,String thoiGianDi,int maChuyen,LopKetNoi ketNoiCSDL)
    {
        boolean deletedVe=false;
        try {
            java.sql.Timestamp tsThoiGianDi=new java.sql.Timestamp(formatter.parse(thoiGianDi).getTime());
            ketNoiCSDL.update("delete from Ve_KhuHoi where MaVe_ChieuDi=? and ThoiGianLenTau_ChieuDi=?",maVe,tsThoiGianDi);
            deletedVe=ketNoiCSDL.update("delete from Ve where maVe=? and ThoiGianLenTau=?",maVe,tsThoiGianDi);
            ketNoiCSDL.update("delete from ChuyenDi where MaChuyen=?",maChuyen);
        } catch (Exception e) {
            System.out.println("lỗi xoá Vé, Ve_KhuHoi, ChuyenDi");
        }
        return deletedVe;
    }
    private void jtfTimKiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfTimKiemKeyReleased
        // TODO add your handling code here:
        if (taiKhoan.getMaLoaiTaiKhoan().equals("KH"))
            {
                if (cbbTimKiem.getSelectedIndex() == 0) {// tim kiem theo ma tuyen
                    String maVe = jtfTimKiem.getText().trim();
                    loadDSVeVaoBang(ketNoiCSDL.select("select * from Ve where TenTaiKhoan=? and maVe like ?",
                            taiKhoan.getTenTaiKhoan(),"%" + maVe + "%"), jtbVeModel);
                } else {// tim kiem theo ten tuyen
                    String soDinhDanh = jtfTimKiem.getText().trim();
                    loadDSVeVaoBang(ketNoiCSDL.select("select * from Ve where TenTaiKhoan=? and SoDinhDanh like ?",
                            taiKhoan.getTenTaiKhoan(),"%" + soDinhDanh + "%"), jtbVeModel);
                }
            }
        else
        {
            if (cbbTimKiem.getSelectedIndex() == 0) {// tim kiem theo ma tuyen
            String maTuyen = jtfTimKiem.getText().trim();
            loadDSVeVaoBang(ketNoiCSDL.select("select * from Ve where maVe like ?",
                    "%" + maTuyen + "%"), jtbVeModel);
            } else {// tim kiem theo ten tuyen
                String tenTuyen = jtfTimKiem.getText().trim();
                loadDSVeVaoBang(ketNoiCSDL.select("select * from Ve where SoDinhDanh like ?",
                        "%" + tenTuyen + "%"), jtbVeModel);
            }
        }
    }//GEN-LAST:event_jtfTimKiemKeyReleased

