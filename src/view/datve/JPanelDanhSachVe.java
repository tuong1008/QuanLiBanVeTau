    private void loadThongTinKhach()
    {
        int maVe=-1;
        try {
            maVe=Integer.valueOf(jtbVeModel.getValueAt(hangDangChon, 0).toString());
        } catch (Exception e) {
            System.out.println("Lỗi chuyển mã vé thành số");
        }
        java.sql.Timestamp thoiGianDi=null;
        try {
            SimpleDateFormat formatter=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss.SSS");
            thoiGianDi=new java.sql.Timestamp(formatter.parse(jtbVeModel.getValueAt(hangDangChon, 1).toString()).getTime());
        } catch (Exception e) {
            System.out.println("Parse Thời gian đi từ bảng bị lỗi");
        }
        try {
            ResultSet rs=ketNoiCSDL.select("select * from Ve where MaVe=? and ThoiGianLenTau=?",maVe,thoiGianDi);
            if (rs.next())
            {
                jtfTen.setText(rs.getString(12));
                jtfSoDinhDanh.setText(rs.getString(13));
            }
        } catch (Exception e) {
            System.out.println("Không tìm được tên và số định danh trong DB");
        }
    }

    private void loadDSVeVaoBang(ResultSet rs,DefaultTableModel model)
    {
        model.setRowCount(0);
        int dem=0;
        try {
            while (rs.next())
            {
                jtbVeModel.addRow(new Object[] {"","","","","","","","",""});
                jtbVeModel.setValueAt(rs.getInt(1), dem, 0);
                jtbVeModel.setValueAt(rs.getTimestamp(2), dem, 1);
                jtbVeModel.setValueAt(rs.getString(3), dem, 2);
                jtbVeModel.setValueAt(rs.getInt(4), dem, 3);
                jtbVeModel.setValueAt(rs.getTimestamp(5), dem, 4);
                jtbVeModel.setValueAt(rs.getInt(8), dem, 5);
                jtbVeModel.setValueAt(rs.getBoolean(9), dem, 6);
                jtbVeModel.setValueAt(rs.getString(10), dem, 7);
                jtbVeModel.setValueAt(rs.getInt(11), dem, 8);
                dem++;
            }
        } catch (Exception e) {
            System.out.println("Không có vé nào");
        }
    }
